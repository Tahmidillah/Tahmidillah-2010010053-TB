    private void updateBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBTActionPerformed
        // TODO add your handling code here:
        String id_makul = idTF.getText();
        String makul = makulTF.getText();
        String sks = sksTF.getText();
        String semester = semesterTF.getText();
        String dosen = dosenTF.getText();
       
        
        try {
            new makul().update(id_makul, makul, sks, semester, dosen );
            JOptionPane.showMessageDialog(this, "Data Berhasil Di Ubah");
            initTabel();
            refresh();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Di Ubah "+e.getMessage());
        }
    }//GEN-LAST:event_updateBTActionPerformed

    private void deletedBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletedBTActionPerformed
        // TODO add your handling code here:
        String id_makul = idTF.getText();
        
        if (idTF.equals("")) {
            JOptionPane.showMessageDialog(this, "id makul tidak boleh kosong");
        }else{
            try {
                new makul().delete(id_makul);
                JOptionPane.showMessageDialog(this, "data berhasil dihapus");
                initTabel();
                refresh();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "data gagal dihapus karena "+e.getMessage());
            }
        }
    }//GEN-LAST:event_deletedBTActionPerformed

