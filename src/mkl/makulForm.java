    private void addBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBTActionPerformed
        // TODO add your handling code here:
        String id_makul =idTF.getText();
        String makul = makulTF.getText();
        String sks = sksTF.getText();
        String semester = semesterTF.getText();
        String dosen = dosenTF.getText();
        
        if (idTF.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "id mata kuliah tidak boleh kosong");
        }else if (makulTF.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "mata kuliah tidak boleh kosong");
        }else if (sksTF.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "sks tidak boleh kosong");
        }else if (semesterTF.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "semester tidak boleh kosong");
        }else if (dosenTF.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "dosen tidak boleh kosong");
        }else{
        
        try {
            new makul().insert(id_makul, makul, sks, semester, dosen);
            JOptionPane.showMessageDialog(this, "Data Berhasil Di Masukkakn");
            initTabel();
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Dimasukkan Karena "+e.getMessage());
        }
    }//GEN-LAST:event_addBTActionPerformed
    }
    private void tableTBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTBMouseClicked
        // TODO add your handling code here:
        addBT.setEnabled(false);
        
        int pilih = tableTB.getSelectedRow();
        String id_makul = tableTB.getValueAt(pilih, 0).toString();
        String makul = tableTB.getValueAt(pilih, 1).toString();
        String sks = tableTB.getValueAt(pilih, 2).toString();
        String semester = tableTB.getValueAt(pilih, 3).toString();
        String dosen = tableTB.getValueAt(pilih, 4).toString();
        
        idTF.setText(id_makul);
        makulTF.setText(makul);
        sksTF.setText(sks);
        semesterTF.setText(semester);
        dosenTF.setText(dosen);
    }//GEN-LAST:event_tableTBMouseClicked

